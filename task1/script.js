// Створюємо масив чисел
const numbers = [10, 20, 30, 40, 50];

// Ініціалізуємо змінну для збереження суми
let sum = 0;

// Використовуємо цикл for для перебору елементів масиву
for (let i = 0; i < numbers.length; i++) {
  // Додаємо поточний елемент масиву до змінної sum
  sum += numbers[i];
}

// Виводимо суму всіх елементів масиву у консоль
console.log("Сума всіх елементів у масиві:", sum);
